package project.test;

public class TestIfItWork {
	private String field;
	private String name;
	private int age;
	private int[] data;
	
	public TestIfItWork(String field, String name, int age, int[] data) {
		this.field = field;
		this.name = name;
		this.age = age;
		this.data = data;
	}

	public String getField() {
		return field;
	}

	public void setField(String field) {
		this.field = field;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public int[] getData() {
		return data;
	}

	public void setData(int[] data) {
		this.data = data;
	}
	
	
	
	
	
}

